grade=float(input("What is your grade in percentage?"))
if grade>=85 and grade<=100:
    print("Wow you have a future, good job.")
elif grade>=70 and grade<=84:
    print("Fine, you can do better though.")
elif grade>=60 and grade<=69:
    print("...Get better.")
elif grade>=50 and grade<=59:
    print("Cancel all distractions. Focus.")
else:
    print("Maybe try a different path. Rethink your life.")